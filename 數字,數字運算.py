# 數字,數字的用法
print(8+7)

print(8-7)

print(8*7)

print(8/7)

# 整數除法
print(8//7)

print(8+8*5)

print((8+8)*5)

number = 8
print(number*5)


# 特殊運算符號
# 取餘數 %
print(number % 5)

# 轉換為字串
print("答案為"+str(number))

# 絕對值
number = -8
print(abs(number))

# 次方
print(pow(number, 2))

# 列表中的最大值
print(max(2, 7, 11, 35, 3, 55, 100))

# 列表中的最大值
print(min(2, 7, 11, 35, 3, 55, 100))

# 四捨五入
number1 = 4.4
print(round(number1))

# 引入函式庫
from math import *

# 無條件捨去(floor),無條件進位(ceil),開根號(sqrt)
print(floor(number1))
print(ceil(number1))
print(sqrt(number1))
